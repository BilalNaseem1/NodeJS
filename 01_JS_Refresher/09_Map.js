// map allows us to transform an array into another array
// map is a higher order function
// map is used to create a new array
// map is used to create a new array with the same length as the original array


const numbers = ['one', 'two', 'three', 'four', 'five'];

// map makes a new array
console.log(numbers.map(hobbies => hobbies.toUpperCase()));

// old array
console.log(numbers);
